package com.avekshaa.cis.reporting;

public class MapDataBean {
	private String stateName;
	private Double stateAvg;
	private Double stateCount;
	
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public Double getStateAvg() {
		return stateAvg;
	}
	public void setStateAvg(Double stateAvg) {
		this.stateAvg = stateAvg;
	}
	public Double getStateCount() {
		return stateCount;
	}
	public void setStateCount(Double stateCount) {
		this.stateCount = stateCount;
	}

	
}
